public class Latihan2 {// ini untuk class scope merangkup semua class yang ada di dalamnya
    public static void main(String[] args) {// ini untuk class scope bagian dari dalam class
        int number=10;//sintaks type data number dengan nama varibel number dan nilainya 10
        System.out.println(number);// menampilkan ke layar
        String name="Citra Febriawirti";//sintaks type data string dengan nama varibel name dan nilainya Citra Febriawirti
        System.out.println(name);// menampilkan ke layar
        float nilai=80.5f;//sintaks type data float dengan nama float number dan nilainya 80.5
        System.out.println(nilai);// menampilkan ke layar
        char huruf='A';//sintaks type data string dengan nama char number dan nilainya A
        System.out.println(huruf);// menampilkan ke layar
        boolean test=true;//sintaks type data boolean dengan nama test number dan nilainya true
        System.out.println(test);// menampilkan ke layar
    }
    
}
