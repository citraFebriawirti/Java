public class TipeDataNumber {
    public static void main(String[] args) {
        byte iniByte = 110;
        short iniShort = 1200;
        int iniInt = 12000000;
        long iniLong = 120000000;
        float iniFloat = 12.5f;
        double iniDouble = 12.6d;
        System.out.println("nilai byte adalah "+ iniByte + "  dari data");
        System.out.println("nilai short adalah "+ iniShort + " dari data");
        System.out.println("nilai Int adalah "+ iniInt + " dari data");
        System.out.println("nilai Long adalah "+ iniLong + " dari data");
        System.out.println("nilai Float adalah "+ iniFloat + " dari data");
        System.out.println("nilai Double adalah "+ iniDouble + " dari data");
        
    }
    
}
