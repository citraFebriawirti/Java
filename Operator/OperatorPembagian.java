public class OperatorPembagian {// ini untuk class scope merangkup semua class yang ada di dalamnya
    public static void main(String[] args) {// ini untuk class scope bagian dari dalam class
        int bilA = 18;// variable bilA memuat nilai 18, termasuk dalam tipe data Int
        int bilB = 2;// variable bilB memuat nilai 2, termasuk dalam tipe data Int
        int hasilBagi = bilA / bilB;//variable jumlah memuat nilai dari varible bilA dan bilB,operator (/) termasuk tipe data int
        System.out.println("A          = " + bilA);//outputnya A = 18
        System.out.println("B          = " + bilB);//outputnya B = 2
        System.out.println("A dibagi B = " + hasilBagi);//outputnya A dibagi B = 2
    }
    
}
