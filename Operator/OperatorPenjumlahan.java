public class OperatorPenjumlahan {// ini untuk class scope merangkup semua class yang ada di dalamnya
    public static void main(String[] args) {// ini untuk class scope bagian dari dalam class
        int nilaiA = 100;// variable nilaiA memuat nilai 100, termasuk dalam tipe data Int
        int nilaiB = 20;// variable nilaiB memuat nilai 20, termasuk dalam tipe data Int
        int jumlah = nilaiA + nilaiB;//variable jumlah memuat nilai dari varible nilaiA dan nilaiB,operator (+) termasuk tipe data int
        System.out.println("Nilai A = " + nilaiA);//outputnya Nilai A = 100
        System.out.println("Nilai B = " + nilaiB);// outputnya Nilai B = 20
        System.out.println("Jumlah  = " + jumlah);// outputnya Jumlah = 120

    }
    
}
